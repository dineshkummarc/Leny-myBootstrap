							<h2 class="cursive-font  primary-color">History</h2>
							<p>On January 2, 1991, the Bureau of Jail Management and Penology was created thru Republic Act 6975 as a line Bureau under the Department of Interior and Local Government. The Jail Bureau  is the upgraded version of its forerunner, the Office of Jail Management and Penology of the defunct PC/INP last headed by  BRIG GEN Arsenio E. Concepcion.</p>

                            <p>As mandated by law,  the  BJMP shall operate under the reorganized  Department of  the  Interior  and  Local Government.</p>

                            <p>Starting  from  scratch with  500 personnel in 1991 the BJMP weaned from its mother PC/INP as a mere component,  to become a full-fledge bureau.  Director Charles S. Mondejar  took his oath of office on July 1 of   1991 as  the first Chief  of  the Bureau.</p>

                            <p>The Bureau of  Jail  Management  and  Penology supervises and controls all district, city and municipal jails. </p>
