							<h1 class="cursive-font  primary-color">Organization</h1>
							<h2>ORGANIZATION</h2>
                            <p>The Bureau of Jail Management and Penology, also referred to as the Jail Bureau, was created pursuant to Section 60 to 65, Chapter V, RA No. 6975, and initially consisting of uniformed officers and members of the Jail Management and Penology service as constituted under Presidential Decree No. 765. RA 9263 provides that the Bureau shall be headed by a Chief who is assisted by two (2) Deputy Chiefs, one (1) for Administration and another for Operations, and one (1) Chief of Directorial Staff, all of whom are appointed by the President upon the recommendation of the DILG Secretary from among the qualified officers with the rank of at least Senior Superintendent in the BJMP. The Chief of the BJMP carries the rank of Director and serves a tour of duty that must not exceed four (4) years, unless extended by the President in times of war and other national emergencies. Officers who have retired or are within six (6) months from their compulsory retirement age are not qualified to be appointed as Jail Director or designated as BJMP Chief.</p>

                            <p>The second officer in command of the BJMP is the Deputy Chief for Administration, the third officer in command is the Deputy Chief for Operations, and the fourth officer in command is The Chief of the Directorial Staff, all of whom carry the rank of Chief Superintendent. They are assisted by the Directors of the Directorates in the National Headquarters who carry the rank of at least Senior Superintendent.</p>
 
                            <p>The BJMP operates and maintains Regional Offices in each of the administrative regions of the country, headed by a Regional Director for Jail Management and Penology, with the rank of at least Senior Superintendent. The Regional Director is assisted by an Assistant Regional Director for Administration, Assistant Regional Director for Operations, and Regional Chief of Directorial Staff, who are all officers with the rank of at least Superintendent.</p>

                            <h3>REGIONAL OFFICE</h3>
                            <p>The BJMP operates and maintains Regional Offices in each of the administrative regions of the country, headed by a Regional Director for Jail Management and Penology, with the rank of at least Senior Superintendent. The Regional Director is assisted by an Assistant Regional Director for Administration, Assistant Regional Director for Operations, and Regional Chief of Directorial Staff, who are all officers with the rank of at least Superintendent.</p>

                            <h4>JAIL PROVINCIAL ADMINISTRATOR'S OFFICE</h4>
                            <p>In every province, the BJMP operates and maintains a Provincial Jail Administratorâ€™s Office headed by a Provincial Administrator, to oversee the implementation of jail services of all district, city and municipal jails within its territorial jurisdiction.</p>

                            <h5>DISTRICT JAIL</h5>
                            <p>Within large cities or a group of clustered municipalities, a District Jail headed by a District Warden may be established.</p>

                            <h6>CITY AND MUNICIPAL JAILS</h6>
                            <p>The BJMP operates and maintains City and Municipal Jails, each headed by a City or Municipal Warden, as the case may be.</p>
