							<h2>Mission</h2>
                            <p>To enhance public safety by ensuring humane safekeeping and development of Persons Deprived of Liberty (PDL) in all district, city, and municipal jails for their integration to society.</p>
                            <h3>Vision</h3>
                            <p>A premier institution highly regarded by society for the secure and humane treatment of Persons Deprived of Liberty (PDL) by its competent and motivated corps.</p>
