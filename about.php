							<h2>Mandate</h2>
                            <p>Bjmp is mandated to direct, supervise and control the administration and operation of all district, city and municipal jails nationwide with pronged tasks of safekeeping and development of inmates.</p>

                            <h3>Major Program</h3>

                            <p>There are four (4) major programs under the mandate of bjmp and they are the following:</p>

                            <p>1. Inmates custody,  security and control program</p>
                            <p>2. Inmates welfare and development program</p>
                            <p>3. Decongestion program</p>
